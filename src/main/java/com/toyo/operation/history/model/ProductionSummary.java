package com.toyo.operation.history.model;

public class ProductionSummary {
	
	public String Weigher;
	public float CO2;
	public float NH3;
	
	
	public String getWeigher() {
		return Weigher;
	}
	public void setWeigher(String weigher) {
		Weigher = weigher;
	}
	public float getCO2() {
		return CO2;
	}
	public void setCO2(float cO2) {
		CO2 = cO2;
	}
	public float getNH3() {
		return NH3;
	}
	public void setNH3(float nH3) {
		NH3 = nH3;
	}
	

}
